# مشروع: الطلاب والدرجات باستخدام القوائم المتداخلة في بايثون
# فكرة المشروع:
# نفس فكرة الكتب، لكن هنا عندنا طلاب ودرجاتهم.
# نستخدم صندوق كبير (قائمة رئيسية) فيه صناديق صغيرة (كل صندوق طالب ودرجته).
# الهدف: نتعلم كيف نخزن البيانات، وكيف نستدعيها، وكيف نضيف ونعدل.

# أول شيء: إنشاء الصندوق الكبير باسم students
# هذا المتغير يمثل القائمة الرئيسية التي تحتوي كل الطلاب.
# كل طالب نمثله بقائمة داخلية فيها اسم الطالب ودرجته.
students = [['student1', 90], ['student2', 85], ['student3', 70]]

# طباعة كل الطلاب مع درجاتهم
# نطبع المتغير students بالكامل عشان نشوف شكله قبل ما نشتغل عليه.
print('كل الطلاب مع درجاتهم:')
print(students)

# استدعاء الطالب الأول (الصندوق الأول)
# students[0] تعني أول عنصر داخل القائمة، وهو ['student1', 90]
print('الطالب الأول (اسم + درجة):')
print(students[0])

# استدعاء الطالب الثاني (الصندوق الثاني)
# students[1] تعني ثاني عنصر داخل القائمة، وهو ['student2', 85]
print('الطالب الثاني (اسم + درجة):')
print(students[1])

# استدعاء الطالب الثالث (الصندوق الثالث)
# students[2] تعني ثالث عنصر داخل القائمة، وهو ['student3', 70]
print('الطالب الثالث (اسم + درجة):')
print(students[2])

# استدعاء اسم الطالب الأول فقط
# students[0][0] تعني: من الصندوق الكبير نأخذ الصندوق الأول، ثم العنصر الأول داخله (الاسم).
print('اسم الطالب الأول فقط:')
print(students[0][0])

# استدعاء درجة الطالب الأول فقط
# students[0][1] تعني: من الصندوق الكبير نأخذ الصندوق الأول، ثم العنصر الثاني داخله (الدرجة).
print('درجة الطالب الأول فقط:')
print(students[0][1])

# استدعاء اسم ودرجة الطالب الثاني بنفس الأسلوب
# نستخدم نفس الفكرة لكن مع الصندوق الثاني.
print('اسم الطالب الثاني:', students[1][0])
print('درجة الطالب الثاني:', students[1][1])

# الآن ننتقل للإضافة باستخدام append
# append نستخدمها إذا أردنا إضافة عنصر جديد في "آخر" القائمة.
# هنا نضيف طالب جديد اسمه student4 ودرجته 95.
students.append(['student4', 95])

# طباعة القائمة بعد إضافة الطالب الجديد
# هذا يساعدنا نتأكد أن الإضافة نجحت وأن الطالب ظهر في النهاية.
print('بعد إضافة الطالب الرابع:')
print(students)

# تعديل درجة طالب موجود
# إذا أردنا تعديل درجة طالب، نستخدم الفهرسة ونغير القيمة مباشرة.
# هنا نعدل درجة الطالب الأول من 90 إلى 92.
students[0][1] = 92

# طباعة بيانات الطالب الأول بعد التعديل للتأكد من التغيير.
print('بيانات الطالب الأول بعد تعديل الدرجة:')
print(students[0])

# الآن نحسب متوسط درجات الطلاب
# أولًا ننشئ متغير اسمه total عشان نجمع فيه كل الدرجات.
total = 0

# نستخدم حلقة for عشان نمر على كل طالب داخل القائمة students.
# كل عنصر في students اسمه student وهو عبارة عن ['الاسم', الدرجة].
for student in students:
    # student[1] هي درجة الطالب، نضيفها إلى total في كل مرة.
    total = total + student[1]

# بعد ما نجمع كل الدرجات، نحسب المتوسط.
# المتوسط = مجموع الدرجات ÷ عدد الطلاب.
# len(students) ترجع لنا عدد الطلاب داخل القائمة.
average = total / len(students)

# طباعة متوسط الدرجات النهائي.
print('متوسط درجات الطلاب:')
print(average)

